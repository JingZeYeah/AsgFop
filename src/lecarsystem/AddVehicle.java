    private void Add(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Add
        // TODO add your handling code here:
        if(evt.getSource() == btnAdd){
            String carplate = txtCarPlate.getText();
            String carmodel = txtCarModel.getText();
            String acqPrice = txtAccPrice.getText();
           
            if(carplate.hashCode() == 0)
            {
                JOptionPane.showMessageDialog(null, "Car plate field is empty!\nPlease fill it up. ", "Alert", JOptionPane.WARNING_MESSAGE);
            }
            else if(carmodel.hashCode() == 0)
            {
                JOptionPane.showMessageDialog(null, "Car model field is empty!\nPlease fill it up. ", "Alert", JOptionPane.WARNING_MESSAGE);
            }
            else if(acqPrice.hashCode() == 0)
            {
                JOptionPane.showMessageDialog(null, "Acquired Price field is empty!\nPlease fill it up. ", "Alert", JOptionPane.WARNING_MESSAGE);
            }
            else if (!acqPrice.matches("-?\\d+(\\.\\d+)?")) 
            {
                JOptionPane.showMessageDialog(null, "Incorrect format at Acquired Price field!\nPlease fill it again. ", "Alert", JOptionPane.WARNING_MESSAGE);
                txtAccPrice.setText("");
            }
            else
            {
                try{
                    
                    insertData();
                     
                }catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Error!!!\nData can't save.\nThere is an existing car plate.", "Alert", JOptionPane.WARNING_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_Add

