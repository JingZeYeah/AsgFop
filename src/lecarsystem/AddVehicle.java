    private void insertData() throws Exception {
        
        String fileName = "vehicletest.csv";
        String carplate = txtCarPlate.getText();
        String carmodel = txtCarModel.getText();
        String acqPrice = txtAccPrice.getText();
        
        //Storing CVS file in ArrayList that will be used later for checking
        Function readCSV = new Function();
        List<String[]> vehicleData = readCSV.readCsvFile(fileName);
        
        boolean fileExists = new File(fileName).exists();
        
         // Check if the data already exists in the ArrayList
        boolean exists = false;
        for (String[] data : vehicleData) {
            if (data[0].equals(carplate)) {
                //JOptionPane.showMessageDialog(null, data[1], "Alert", JOptionPane.WARNING_MESSAGE);
                exists = true;
                break;
            }
        }
        
        if (!exists) {
            // Add data to the ArrayList
            String[] data = {carplate, carmodel, acqPrice , "1", ""};
            vehicleData.add(data);

            // Save data to CSV file
            try {
                FileWriter fw = new FileWriter(fileName, true);

                // Add header only if the file created for the first time
                if (!fileExists) { 
                    fw.append("carPlate,carModel,acquirePrice,carStatus,salesPrice\r\n");
                }
                // Append the data to the CSV file
                for (String value : data) {
                    fw.append(value).append(",");
                }
                fw.append("\r\n");
                fw.flush();
                fw.close();
                JOptionPane.showMessageDialog(null, "Data has been saved.");
                txtCarPlate.setText("");
                txtCarModel.setText("");
                txtAccPrice.setText("");
                
            } catch (IOException e) {
                System.err.println("An error occurred while creating the CSV file: " + e.getMessage());
                JOptionPane.showMessageDialog(null, "Error!!!\nThe CSV file is in used.\nPlease make sure it is close when in use.", "Alert", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Error!!!\nData can't save.\nThere is an existing car plate.", "Alert", JOptionPane.WARNING_MESSAGE);
        }
    }
