    private void Search(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Search
        // TODO add your handling code here:
        if(evt.getSource() == btnSearch){
            String word = txtWord.getText();
            String salesInput = txtSalesPrice.getText();
            String accquireInput = txtAccPrice.getText();
            String carStatus = txtStatus.getText();
            int sales,accquire;
            String status;

            if (salesInput.isEmpty()){ sales = 0;}
            else {sales = Integer.parseInt(salesInput);}
            
            if (accquireInput.isEmpty()){ accquire = 0;}
            else {accquire = Integer.parseInt(accquireInput);}
            
            if (carStatus.isEmpty()){ status = "10";}
            else {status = carStatus;}
            
            try{
                searchfilter(word,sales, accquire,status);
            }catch (Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_Search

